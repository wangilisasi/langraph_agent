"""LangGraph agent built with explicit Nodes and Edges."""

import atexit
from typing import Annotated, TypedDict

from dotenv import load_dotenv

load_dotenv()

import os
from langchain_openai import ChatOpenAI
from langchain_core.messages import AnyMessage, HumanMessage, SystemMessage
from langgraph.checkpoint.sqlite import SqliteSaver
from langgraph.graph import StateGraph, START, END
from langgraph.graph.message import add_messages
from langgraph.prebuilt import ToolNode

from tools import (
    web_search,
    fetch_page,
    get_current_datetime,
    save_to_file,
    read_file,
)

# ?????? State ????????????????????????????????????????????????????????????????????????????????????????????????????????????????????????????????????????????????????????????????????????????????????????????????
# This TypedDict defines the data that flows through every node.
# `add_messages` is a reducer that appends new messages instead of replacing.


class AgentState(TypedDict, total=False):
    messages: Annotated[list[AnyMessage], add_messages]
    plan: str


# ?????? Model ????????????????????????????????????????????????????????????????????????????????????????????????????????????????????????????????????????????????????????????????????????????????????????????????
# OpenRouter is OpenAI-compatible, so we reuse ChatOpenAI with a different
# base_url and api_key. You can swap the model to any on OpenRouter:
# https://openrouter.ai/models
tools = [
    web_search,
    fetch_page,
    get_current_datetime,
    save_to_file,
    read_file,
]

llm = ChatOpenAI(
    # model="openai/gpt-5-nano",
    model="moonshotai/kimi-k2.5",                   # OpenRouter model ID
    base_url="https://openrouter.ai/api/v1",     # point to OpenRouter
    api_key=os.getenv("OPENROUTER_API_KEY"),      # use the OpenRouter key
    temperature=0,
    max_retries=2,                               # Handle temporary network failures
    timeout=30.0,                                # Don't hang forever
)
llm_with_tools = llm.bind_tools(tools)

SYSTEM_PROMPT = SystemMessage(
    content=(
        "You are a research assistant. Your job is to help users find, "
        "analyse, and organise information from the web.\n\n"
        "Guidelines:\n"
        "- Use web_search when you need current or real-time information.\n"
        "- Use fetch_page when the user gives you a specific URL.\n"
        "- Save important findings with save_to_file.\n"
        "- Always cite your sources with URLs.\n"
        "- Be concise but thorough."
    )
)

PLANNER_PROMPT = SystemMessage(
    content=(
        "You are the planning layer for a research agent. Create a short execution "
        "plan for the latest user request before the agent starts working.\n\n"
        "Rules:\n"
        "- If the question is a simple, well-known fact (e.g. capitals, math, "
        "definitions), plan a single step: 'Respond directly from your own knowledge.'\n"
        "- Only plan tool use when the question requires current/real-time info, "
        "web research, file operations, or specifics you might not know.\n"
        "- Return 2 to 5 numbered steps.\n"
        "- Mention which tool is likely useful when relevant.\n"
        "- Prefer short, practical steps.\n"
        "- If the request is simple, return a one-step plan.\n"
        "- Do not answer the user directly.\n"
        "- Do not use markdown headings or extra commentary."
    )
)


# ?????? Nodes ????????????????????????????????????????????????????????????????????????????????????????????????????????????????????????????????????????????????????????????????????????????????????????????????
# Each node is a function that receives the current state and returns
# a partial state update (here, new messages to append).


def chatbot(state: AgentState) -> AgentState:
    """Call the LLM. It may decide to invoke tools or reply directly."""
    messages = [SYSTEM_PROMPT]

    plan = state.get("plan", "").strip()
    if plan:
        messages.append(
            SystemMessage(
                content=(
                    "Execution plan for this turn:\n"
                    f"{plan}\n\n"
                    "Follow this plan. If it says to respond directly, do NOT call any tools. "
                    "Update it mentally if new evidence appears, "
                    "but do not expose the plan unless the user asks for it."
                )
            )
        )

    messages += state["messages"]
    response = llm_with_tools.invoke(messages)
    return {"messages": [response]}


def planner(state: AgentState) -> AgentState:
    """Create a lightweight plan for the latest user request."""
    latest_user_message = next(
        (msg for msg in reversed(state["messages"]) if isinstance(msg, HumanMessage)),
        None,
    )

    if latest_user_message is None:
        return {"plan": "1. Respond directly using the available context."}

    plan_response = llm.invoke([PLANNER_PROMPT, latest_user_message])
    plan_text = (plan_response.content or "").strip()
    if not plan_text:
        plan_text = "1. Inspect the request and answer directly if no tool use is needed."

    return {"plan": plan_text}


# ToolNode automatically executes whichever tool the LLM requested.
tool_node = ToolNode(tools=tools)


# ?????? Conditional edge ????????????????????????????????????????????????????????????????????????????????????????????????????????????????????????????????????????????????????????????
# After the chatbot node we need to decide: did the LLM call a tool,
# or did it produce a final answer?


def should_continue(state: AgentState) -> str:
    """Route to 'tools' if the last message has tool calls, else 'end'."""
    last_message = state["messages"][-1]
    if last_message.tool_calls:
        return "tools"
    return END


# ?????? Build the graph ???????????????????????????????????????????????????????????????????????????????????????????????????????????????????????????????????????????????????????????????
graph = StateGraph(AgentState)

# 1. Add nodes
graph.add_node("planner", planner)
graph.add_node("chatbot", chatbot)
graph.add_node("tools", tool_node)

# 2. Add edges
graph.add_edge(START, "planner")              # entry point
graph.add_edge("planner", "chatbot")        # plan first, then act
graph.add_conditional_edges("chatbot", should_continue)  # chatbot ??? tools | END
graph.add_edge("tools", "chatbot")            # after tools, loop back to chatbot

# 3. Compile into a runnable
CHECKPOINT_DB_PATH = os.getenv("CHECKPOINT_DB_PATH", "output/agent_checkpoints.db")
_checkpointer_cm = SqliteSaver.from_conn_string(CHECKPOINT_DB_PATH)
checkpointer = _checkpointer_cm.__enter__()


def _close_checkpointer() -> None:
    _checkpointer_cm.__exit__(None, None, None)


atexit.register(_close_checkpointer)
agent = graph.compile(checkpointer=checkpointer)


